    @Override
    public boolean setComboBoxInitial() {
        DefaultComboBoxModel x = new DefaultComboBoxModel();
        DefaultComboBoxModel y = new DefaultComboBoxModel();
        try {
            CtrlVenEstadoVenta estadoG = new CtrlVenEstadoVenta();
            CtrlVenPresupuesto presupuesto = new CtrlVenPresupuesto();
            ArrayList<Object> lista = estadoG.ConsultaCompleja();
            ArrayList<Object> listaP = presupuesto.ConsultaCompleja();
            if (this.idAccion == 0) {
                //Agregamos combo de estados de venta
                //leyenda seleccionar
                x.addElement("SELECCIONA");
                for (int i = 0; i < lista.size(); i++) {
                    VenEstadoVenta tempObject = (VenEstadoVenta) lista.get(i);
                    x.addElement(tempObject.getStrValor());
                }
                cmbEstado.setModel(x);
                //Agregamos presupuestos
                y.addElement("SELECCIONA");
                for (int i = 0; i < lista.size(); i++) {
                    VenPresupuesto tempObject = (VenPresupuesto) listaP.get(i);
                    y.addElement(tempObject);
                }
                cmbVenPresupuesto.setModel(y);

            } else {
                //Agregamos estados de venta
                for (int i = 0; i < lista.size(); i++) {
                    VenEstadoVenta tempObject = (VenEstadoVenta) lista.get(i);
                    x.addElement(tempObject.getStrValor());

                }
                cmbEstado.setModel(x);
                //Agregamos presupuestos
                for (int i = 0; i < lista.size(); i++) {
                    VenPresupuesto tempObject = (VenPresupuesto) listaP.get(i);
                    y.addElement(tempObject);
                }
                cmbVenPresupuesto.setModel(y);
                this.setComboBox();
            }

            return true;
        } catch (SQLException ex) {
            Logger.getLogger(JdMarcaManager.class.getName()).log(Level.SEVERE, null, ex);
            return false;
        }

    }

    @Override
    public boolean setLoadInformation() {
        this.setInformacion(this.idAccion);
        if (this.setComboBoxInitial() == false) {
            this.dispose();
        }
        this.setTextField();
        this.setComboBox();
        this.setTabla();
        this.setFecha();
        this.setCheck();
        this.setExtra();

        return true;
    }

    @Override
    public void setComboBox() {
        if (this.idEstado > 0) {
            this.cmbEstado.setSelectedIndex(this.idEstado - 1);
            for (int i = 0; i < this.cmbVenPresupuesto.getItemCount(); i++) {
                VenPresupuesto presupuesto = (VenPresupuesto) this.cmbVenPresupuesto.getItemAt(i);
                if (this.idPresupuesto == presupuesto.getId()) {
                    this.cmbVenPresupuesto.setSelectedIndex(i);
                }
            }
        }

    }

