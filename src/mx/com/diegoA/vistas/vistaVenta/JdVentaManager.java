    @Override
    public Object showDialog(Object _obj) {
        this.baseEntity = (VenVenta) _obj;
        this.idAccion = (this.baseEntity != null) ? this.baseEntity.getId() : 0;
        if (this.idAccion == 0) {
            this.baseEntity = new VenVenta();
        }
        this.idEstado = this.baseEntity.getIdVenEstadoVenta();//obtener el id de estado para el combo de editar
        this.idPresupuesto = this.baseEntity.getIdVenPresupuesto();
        this.setLoadInformation();
        this.setModalityType(Dialog.ModalityType.APPLICATION_MODAL);
        //this.setBounds(600, 500, 500, 500);
        this.setVisible(true);
        return (this.baseEntity != null) ? this.baseEntity : null;
    }

    @Override
    public boolean validaDatos() {

        if (this.idAccion == 0) {
            if (this.cmbEstado.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Seleccione una opción valida de Estado de Venta");
                return false;
            }

            if (this.cmbVenPresupuesto.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Seleccione una opción valida de Presupuesto");
                return false;
            }

        }

        return true;
    }

