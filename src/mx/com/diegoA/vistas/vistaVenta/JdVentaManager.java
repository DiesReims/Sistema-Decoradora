    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        try {
            if (this.validaDatos() == true) {
                java.util.Date date = this.DtFecha.getDate();
                long d = date.getTime();
                java.sql.Date fecha = new java.sql.Date(d);
                this.baseEntity.setDtFecha(fecha);
                //aqui falta la linea para obtener el combo seleccionado
                if (this.idAccion == 0) {
                    this.baseEntity.setIdVenEstadoVenta(this.cmbEstado.getSelectedIndex());
                } else {
                    this.baseEntity.setIdVenEstadoVenta(this.cmbEstado.getSelectedIndex() + 1);
                }
                //se cierra la pantalla
                this.dispose();
            }

        } catch (Exception e) {
            System.out.println(e.getMessage());
            System.out.println(e.getLocalizedMessage());
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

